package com.lifesparktech.lsphysio.android.data
data class InventoryItem(
    val itemName: String,           // Name or description of the item
    val itemCode: String,           // Unique identifier (e.g., SKU)
    val category: String,           // Category of the item (e.g., Electronics, Office Supplies)
    val quantityInStock: Int,       // Current stock level
    val unitOfMeasure: String,      // How the item is measured (e.g., pieces, liters)
    val location: String,           // Location of the item (e.g., Warehouse, Shelf)
    val supplier: String,           // Supplier or Vendor name
    val purchasePrice: Double,      // Purchase price per unit
    val sellingPrice: Double,       // Selling price per unit (if applicable)
    val reorderLevel: Int,          // Minimum stock level to trigger reorder
    val reorderQuantity: Int,       // Quantity to reorder when stock reaches reorder level
    val leadTime: Int,              // Lead time in days to restock
    val stockStatus: StockStatus,   // Current stock status (In Stock, Out of Stock, Low Stock)
    val lastRestockDate: String?,   // Date the item was last restocked (nullable)
    val expirationDate: String?,    // Expiration date (nullable for non-perishables)
    val condition: ItemCondition,   // Condition of the item (New, Used, etc.)
    val qualityCheckDate: String?,  // Last quality check date (nullable)
    val damageOrDefects: String?,   // Any damage or defect info (nullable)
    val warrantyPeriod: Int?,       // Warranty period in months (nullable)
    val batchNumber: String?,       // Batch number or serial number (nullable)
    val dateOfPurchase: String,     // Date of purchase
    val invoiceNumber: String?,     // Invoice number (nullable)
    val assignedTo: String?,        // Assigned person or department (nullable)
    val usageRate: Double?,         // Average consumption rate (nullable)
    val customTags: List<String> = emptyList(), // Custom tags for grouping (optional)
    val storageRequirements: String?, // Storage instructions (nullable)
    val environmentalImpact: String?, // Environmental impact info (nullable)
    val complianceInfo: String?     // Compliance or certification info (nullable)
)
enum class StockStatus {
    IN_STOCK,
    OUT_OF_STOCK,
    LOW_STOCK
}
enum class ItemCondition {
    NEW,
    USED,
    REFURBISHED,
    DAMAGED
}
